            deleted_count = session.query(TraderBranch).filter(
                TraderBranch.trader_id == trader.id
            ).delete(synchronize_session=False)  # ä½¿ç”¨synchronize_session=Falseé¿å…ä¼šè¯åŒæ­¥é—®é¢˜
